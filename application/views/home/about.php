<h1 class="h4 text-gray-900 mb-4"><b>Prosedur bergabung ke Badan Pamsimas Nagari Limo Koto</b></h1>

<ol>
<li>Membuat akun/melakukan registrasi di akun website pamsimas</li>
<li>Melakukan pembayaran bulan pertama (setiap pelanggan yg melakukan registrasi di bulan ini bisa mulai melakukan pembayaran di bulan depan) untuk no rekening pembayaran terdapat dihalamana contact</li>
<li>Menghubungi teknisi air untuk melakukan pemasukan air (contact teknisi ada di halaman contact), pelanggan tinggal menunggu antrian dan menunggu teknisi datang kerumah untuk melakukan transaksi pembelian peralatan secara langsung</li>
</ol>

<p>Note : Bagi pelanggan yang melakukan penunggakan atau keterlambatan dalam pembayaran sampai 3 bulan agar melapor ke bagian teknisi untuk menyambungkan kembali aliran air yang telah diputus setelah melakukan pembayaran.</p>
