<p>Program Penyediaan Air Minum dan Sanitasi Berbasis Masyarakat, atau dikenal dengan sebutan PAMSIMAS, merupakan platformpembangunan air minum dan sanitasi perdesaan yang dilaksanakan dengan pendekatan berbasis masyarakat.  Program PAMSIMAS I (2008-2012) dan PAMSIMAS II (2013-2015), telah berhasil menambah akses air minum aman bagi 10,4 juta jiwa dan akses sanitasi layak bagi 10,4 juta jiwa di  lebih dari 12.000 desa/kelurahan yang tersebar di 233 kabupaten/kota di 32 provinsi di Indonesia.</p>

<p>Saat ini Program PAMSIMAS memasuki fase ketiga (PAMSIMAS III) yang dilaksanakan pada kurun waktu 2016-2020, dan akan menyasar 15.000 desa sasaran baru serta mengelola keberlanjuran program di hampir lebih dari 27.000 desa peserta PAMSIMAS di seluruh Indonesia.</p>

